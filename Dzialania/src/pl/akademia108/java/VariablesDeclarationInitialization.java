package pl.akademia108.java;

public class VariablesDeclarationInitialization {
    public void variablesDeclarationInitializationExample() {

        /* Deklaracja zmiennej */
        int age;

        /* Inicjalizacja zmiennej */
        age = 21;

        System.out.println(age);

        /* Przypisywanie nowej wartości do zmiennej */
        int age2;

        age2 = age + 9;

        System.out.println(age2);

        /* Deklaracja z inicjalizacją */
        double price = 12.99;

        System.out.println(price);

        /* Przypisywanie nowej wartości do zmiennej */
        double price2;
        price2 = price + 3;

        System.out.println(price2);





    }

}
